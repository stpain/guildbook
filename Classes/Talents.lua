function Talents:GetTalentID(spellID)

    for k, v in ipairs(talentDataWrath) do
        if (v[13] == spellID) or (v[14] == spellID) or (v[15] == spellID) or (v[16] == spellID) or (v[17] == spellID) then
            return v[1]
        end
    end
    return false;
end

function Talents:GetTalentSpellId(fileName, row, col, rank)

    local tabID;
    for k, v in ipairs(talentTabCSV) do
        if v[3] == fileName then
            tabID = v[1]
        end
    end

    if tabID then
        for k, v in ipairs(talentDataWrath) do
            if (v[5] == tabID) and (v[2] == (row - 1)) and (v[4] == (col - 1)) then --v[1] id, v[2] row, v[4] col, v[13] rank 1 spellID, v[14] rank 2 ...
                -- local link = GetSpellLink(v[12 + (rank - 1)])
                -- local name, rank = GetSpellInfo(v[12 + (rank - 1)])
                -- print(name, rank)
                -- return string.format("|Hspell:%d" ,v[12 + (rank - 1)])

                if rank == 0 then
                    return v[13];
                else
                    return v[12 + (rank)];
                end
            end
        end
    end
end

function Talents:GetPlayerTalentInfo()
    local talents = {}
    local tabs = {}
    for tabIndex = 1, GetNumTalentTabs() do
        local spec, texture, pointsSpent, fileName = GetTalentTabInfo(tabIndex)
        local engSpec = self.TalentBackgroundToSpec[fileName]
        table.insert(tabs, {
            fileName = fileName,
            pointsSpent = pointsSpent,
        })
        for talentIndex = 1, GetNumTalents(tabIndex) do
            local name, iconTexture, row, column, rank, maxRank, isExceptional, available = GetTalentInfo(tabIndex, talentIndex)
            local spellId = Talents:GetTalentSpellId(fileName, row, column, rank)
            table.insert(talents, {
                tabID = tabIndex,
                row = row,
                col = column,
                rank = rank,
                maxRank = maxRank,
                spellId = spellId,
            })
        end
    end
    -- find the tab with most points and set spec if not already set, the user can always change this if wrong and this will probably cause them to actually update it.
    table.sort(tabs, function(a, b)
        return a.pointsSpent > b.pointsSpent;
    end)
    return {
        tabs = tabs,
        talents = talents,
    }
end


